uint32_t reverseBits(uint32_t n) {
    uint32_t out = 0;
    for(int i = 0;i < 32;i++){
        if(n % 2 != 0){out++;}
        if(i == 31){break;}
        out<<=1;
        n>>=1;
    }
    return out;
}

